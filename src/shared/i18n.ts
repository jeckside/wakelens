export const isLocaleCode = (value: string): value is LocaleCode =>
  (supportedLocales as readonly string[]).includes(value);

export const normalizeLocale = (value?: string): LocaleCode => {
  if (!value) return defaultLocale;
  if (isLocaleCode(value)) return value;
  const lower = value.toLowerCase();
  if (lower.startsWith('zh')) return 'zh-Hans';
  if (lower.startsWith('hi')) return 'hi';
  if (lower.startsWith('es')) return 'es';
  if (lower.startsWith('ar')) return 'ar';
  if (lower.startsWith('ru')) return 'ru';
  return defaultLocale;
};

export const isRtlLocale = (locale: LocaleCode): boolean => rtlLocales.includes(locale);

export const t = (locale: LocaleCode, key: TranslationKey, vars: Vars = {}): string => {
  const template = translations[locale][key] ?? translations[defaultLocale][key];
  return template.replace(/\{(\w+)\}/g, (_match, name: string) => String(vars[name] ?? ''));
};
